// Tipos de váriaveis mais comuns
// Número inteiro
var numInt = 1;
console.log(numInt);
console.log(typeof numInt);

// string
let varString = "isso é uma string";
console.log(varString);
console.log(typeof varString);

// Boolean
let a = 5
let b = 5
let c = 4
console.log(a == b);
console.log(a == c);

// Undefined
var varUnd;
console.log(varUnd);
console.log(typeof varUnd);

// Null
var varNull = null;
console.log(varNull);
console.log(typeof varNull);

// Symbol
var symVar = Symbol ("$");
console.log(symVar);
console.log(typeof symVar);

